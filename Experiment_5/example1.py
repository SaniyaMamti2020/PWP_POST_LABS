# Python program to count the occurrences of an element in a tuple

# Sample tuple
my_tuple = (1, 2, 3, 4, 2, 2, 5, 3, 2)

# Element to count
element = 2

# Using count() method
occurrences = my_tuple.count(element)

# Display result
print(f"The element {element} occurs {occurrences} times in the tuple.")
